/* Macros for Hex Codes of Common 16-Bit Colors */
#ifndef _BB8_COLORS_H
#define _BB8_COLORS_H


#define	BLACK   0x0000
#define WHITE   0xFFFF

#define	RED     0xF800

#define YELLOW  0xFFE0

#define	GREEN   0x07E0

#define	BLUE    0x001F
#define CYAN    0x07FF

#define MAGENTA 0xF81F


#endif //_BB8_COLORS_H
