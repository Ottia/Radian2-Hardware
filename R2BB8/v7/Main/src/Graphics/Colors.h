/* Macros for Hex Codes of Common 16-Bit Colors */
#ifndef _BB8_COLORS_H
#define _BB8_COLORS_H

typedef uint16_t Color;

//System-Wide Background Color
const Color  BGCOLOR =    0xFADE;

//Special Color Code Indicating No Color Should be Used for the Given Object
const Color   NO_COLOR =  0xFFFE;

const Color	  BLACK =     0x0000;
const Color   WHITE =     0xFFFF;

const Color   RED =       0xF800;

const Color   YELLOW =    0xFFE0;

const Color   GREEN =     0x07E0;

const Color   BLUE =      0x001F;
const Color   CYAN =      0x07FF;

const Color   MAGENTA =   0xF81F;


#endif //_BB8_COLORS_H
